from bisect import bisect_right
def main():
    # Sieve
    primes = []
    multiples = set()
    n = 30000
    for i in range(2, n+1):
        if i not in multiples:
            primes.append(i)
            for j in range(i*i, n+1, i):
                multiples.add(j)
    cases = int(input())
    ans_list = []
    for _ in range(cases):
        d = int(input())
        div1 = primes[bisect_right(primes, d)]
        div2 = min(div1*div1, primes[bisect_right(primes, div1+d-1)])
        ans = div1*div2
        ans_list.append(str(ans))

    print('\n'.join(ans_list))

