def main():
    q = int(input())
    ans_list = []
    for _ in range(q):
        u, v = map(int, input().split(' '))
        if u > v:
            ans_list.append("NO")
        else:
            set_bits_u, set_bits_v = [], []
            for j in range(31, -1, -1):
                if ((1 << j) & u):
                    set_bits_u.append(j)
                if ((1 << j) & v):
                    set_bits_v.append(j)
            
            success = True
            while set_bits_v:
                if not set_bits_u or set_bits_u[-1] > set_bits_v[-1]:
                    success = False
                    break
                set_bits_v.pop()
                set_bits_u.pop()
            ans_list.append("YES" if success else "NO")

    print('\n'.join(ans_list))

